const sum = (a, b) => a + b;
const difference = (a, b) => a - b;
const product = (a, b) => a * b;
const quotient = (a, b) => a / b;

const modulo = (a, b) => a % b;

const power = (a, b) => a ** b;
const log = (a, b) => a * Math.log(b);

const exp = (a, b) => a * Math.exp(b);

const sqrt = (a, b) => a * Math.sqrt(b);

const cbrt = (a, b) => a * Math.cbrt(b);

export {
	sum,
	difference,
	product,
	quotient,
	modulo,
	power,
	log,
	exp,
	sqrt,
	cbrt
};
